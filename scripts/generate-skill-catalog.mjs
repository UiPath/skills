#!/usr/bin/env node
import { constants as fsConstants } from "node:fs";
import {
    access,
    mkdir,
    readdir,
    readFile,
    rm,
    writeFile,
} from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const SCHEMA_VERSION = 1;
const GENERATED_BY = "uipath-cli.skills.catalog";
const MARKER = "<!-- Generated by uip skills catalog. Safe to overwrite. -->";
const REPOSITORY = "https://github.com/UiPath/skills.git";
const CATALOG_SKILL_NAME = "uipath-skill-catalog";
const CATALOG_JSON = "skills.catalog.json";
const CATALOG_README = "README.md";
const CATALOG_INDEX = "INDEX.md";

function parseScalar(value) {
    const trimmed = value.trim();
    if (
        (trimmed.startsWith('"') && trimmed.endsWith('"')) ||
        (trimmed.startsWith("'") && trimmed.endsWith("'"))
    ) {
        return trimmed
            .slice(1, -1)
            .replace(/\\"/g, '"')
            .replace(/\\'/g, "'");
    }
    return trimmed;
}

function parseFrontmatter(content) {
    const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
    if (!match) return {};

    const meta = {};
    let nestedKey = null;
    for (const line of match[1].split(/\r?\n/)) {
        if (!line.trim() || line.trimStart().startsWith("#")) continue;

        const nested = line.match(/^\s+([A-Za-z0-9_-]+):\s*(.*)$/);
        if (nested && nestedKey) {
            meta[nestedKey][nested[1]] = parseScalar(nested[2]);
            continue;
        }

        const scalar = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
        if (!scalar) continue;
        const [, key, value] = scalar;
        if (!value.trim()) {
            meta[key] = {};
            nestedKey = key;
            continue;
        }
        meta[key] = parseScalar(value);
        nestedKey = null;
    }
    return meta;
}

function markdownEscape(value) {
    return value.replace(/\|/g, "\\|").replace(/\r?\n/g, " ");
}

async function exists(filePath) {
    try {
        await access(filePath, fsConstants.F_OK);
        return true;
    } catch {
        return false;
    }
}

async function discoverSkills(skillsRoot) {
    const entries = await readdir(skillsRoot, { withFileTypes: true });
    const skills = [];
    for (const entry of entries) {
        if (!entry.isDirectory()) continue;
        if (entry.name === CATALOG_SKILL_NAME) continue;
        const skillMd = path.join(skillsRoot, entry.name, "SKILL.md");
        if (!(await exists(skillMd))) continue;
        const content = await readFile(skillMd, "utf8");
        const meta = parseFrontmatter(content);
        const name =
            typeof meta.name === "string" && meta.name.trim()
                ? meta.name.trim()
                : entry.name;
        const description =
            typeof meta.description === "string" ? meta.description.trim() : "";
        const metadata =
            meta.metadata &&
            typeof meta.metadata === "object" &&
            !Array.isArray(meta.metadata)
                ? meta.metadata
                : undefined;
        skills.push({
            name,
            description,
            skillPath: `${entry.name}/SKILL.md`,
            ...(metadata ? { metadata } : {}),
        });
    }
    return skills.sort((a, b) => a.name.localeCompare(b.name));
}

function buildCatalog(skills) {
    return {
        schemaVersion: SCHEMA_VERSION,
        generatedBy: GENERATED_BY,
        source: { repository: REPOSITORY },
        scope: "source",
        skills,
    };
}

function renderReadme(catalog, { skillPathPrefix = "" } = {}) {
    const lines = [
        MARKER,
        "",
        "# UiPath Skill Catalog",
        "",
        "Generated overview for the available UiPath skills. Read this file first when a user asks for UiPath help and no single skill is already obvious.",
        "",
        "## How to Use",
        "",
        '1. Search the catalog with `uip skills search "<goal>" --output json`, or inspect `skills.catalog.json` directly.',
        "2. Pick the skill whose description best matches the user's goal and boundaries.",
        "3. Open only that skill's `SKILL.md`, then follow its links into `references/` only as needed.",
        "4. If several skills match, prefer the most specific skill first and add platform/deploy skills only when the task needs them.",
        "",
        "## Available Skills",
        "",
        "| Skill | When to use | Entry point |",
        "|---|---|---|",
    ];
    for (const skill of catalog.skills) {
        lines.push(
            `| \`${markdownEscape(skill.name)}\` | ${markdownEscape(
                skill.description || "(no description)",
            )} | [SKILL.md](${markdownEscape(`${skillPathPrefix}${skill.skillPath}`)}) |`,
        );
    }
    lines.push(
        "",
        "For the complete machine-readable catalog, read `skills.catalog.json`.",
        "",
    );
    return lines.join("\n");
}

function renderIndex(catalog, { skillPathPrefix = "" } = {}) {
    const lines = [
        MARKER,
        "",
        "# UiPath Skills Index",
        "",
        `Schema version: ${catalog.schemaVersion}`,
        `Source: ${catalog.source.repository}`,
        "Scope: source",
        "",
    ];
    for (const skill of catalog.skills) {
        lines.push(
            `## ${skill.name}`,
            "",
            skill.description || "(no description)",
            "",
            `Entry point: [${skill.skillPath}](${skillPathPrefix}${skill.skillPath})`,
            "",
        );
    }
    return lines.join("\n");
}

function renderCatalogSkill(catalog) {
    const lines = [
        "---",
        `name: ${CATALOG_SKILL_NAME}`,
        'description: "[PREVIEW] UiPath skill catalog router. Use first for ambiguous UiPath requests to choose one installed skill before opening full SKILL.md bodies."',
        "---",
        "",
        MARKER,
        "",
        "# UiPath Skill Catalog",
        "",
        "Use this skill first when the user asks for UiPath help and no single specialist skill is already obvious.",
        "",
        "## When to Use This Skill",
        "",
        "- The user asks for UiPath help, but no single specialist skill is obvious from files or domain language.",
        "- The task may involve multiple UiPath products and needs a compact routing pass before implementation.",
        "- A planner or agent needs to shortlist skills without opening every full `SKILL.md` body.",
        "",
        "## Critical Rules",
        "",
        "1. Use the catalog for routing only; do not treat it as the implementation guide.",
        "2. Do not open every specialist `SKILL.md` to compare options.",
        "3. Open only the selected specialist skill, then follow that skill's references as needed.",
        "4. If a file type or product signal already makes one specialist skill obvious, use that specialist directly.",
        "",
        "## Routing Workflow",
        "",
        '1. Read `references/skills.catalog.json` or run `uip skills search "<goal>" --output json`.',
        "2. Pick the skill whose description best matches the user's goal and boundaries.",
        "3. Open only that skill's `SKILL.md`, then follow that skill's references only as needed.",
        "4. If several skills match, prefer the most specific skill first and add platform/deploy skills only when the task needs them.",
        "",
        "## Available Skills",
        "",
        "| Skill | When to use | Entry point |",
        "|---|---|---|",
    ];
    for (const skill of catalog.skills) {
        lines.push(
            `| \`${markdownEscape(skill.name)}\` | ${markdownEscape(
                skill.description || "(no description)",
            )} | [SKILL.md](../${markdownEscape(skill.skillPath)}) |`,
        );
    }
    lines.push(
        "",
        "The complete machine-readable catalog is `references/skills.catalog.json`.",
        "",
        "## What NOT to Do",
        "",
        "- Do not use the catalog as a substitute for the selected specialist skill.",
        "- Do not load broad sets of specialist skill bodies just to decide routing.",
        "- Do not force catalog routing when the user explicitly asks for a known specialist surface.",
        "",
    );
    return lines.join("\n");
}

async function writeOutput(filePath, content) {
    await mkdir(path.dirname(filePath), { recursive: true });
    await writeFile(filePath, content);
}

async function main() {
    const repoRoot = path.resolve(
        path.dirname(fileURLToPath(import.meta.url)),
        "..",
    );
    const skillsRoot = path.join(repoRoot, "skills");
    const catalog = buildCatalog(await discoverSkills(skillsRoot));
    const catalogSkillDir = path.join(skillsRoot, CATALOG_SKILL_NAME);
    const outputs = new Map([
        [
            path.join(skillsRoot, CATALOG_JSON),
            `${JSON.stringify(catalog, null, 2)}\n`,
        ],
        [path.join(skillsRoot, CATALOG_README), renderReadme(catalog)],
        [path.join(skillsRoot, CATALOG_INDEX), renderIndex(catalog)],
        [path.join(catalogSkillDir, "SKILL.md"), renderCatalogSkill(catalog)],
        [
            path.join(catalogSkillDir, "references", CATALOG_JSON),
            `${JSON.stringify(catalog, null, 2)}\n`,
        ],
        [
            path.join(catalogSkillDir, "references", CATALOG_README),
            renderReadme(catalog, { skillPathPrefix: "../../" }),
        ],
        [
            path.join(catalogSkillDir, "references", CATALOG_INDEX),
            renderIndex(catalog, { skillPathPrefix: "../../" }),
        ],
    ]);

    if (process.argv.includes("--check")) {
        let failed = false;
        for (const [filePath, expected] of outputs) {
            const actual = (await exists(filePath))
                ? await readFile(filePath, "utf8")
                : null;
            if (actual !== expected) {
                console.error(`Catalog is stale: ${path.relative(repoRoot, filePath)}`);
                failed = true;
            }
        }
        process.exitCode = failed ? 1 : 0;
        return;
    }

    await rm(catalogSkillDir, { recursive: true, force: true });
    for (const [filePath, content] of outputs) {
        await writeOutput(filePath, content);
    }
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
    main().catch((error) => {
        console.error(error);
        process.exitCode = 1;
    });
}
